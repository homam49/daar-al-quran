<script>
// Search functionality
function searchStudents() {
    const searchInput = document.getElementById('studentSearchInput');
    const searchTerm = searchInput.value.toLowerCase().trim();
    const tableRows = document.querySelectorAll('#studentsTable tbody tr');
    const noResultsDiv = document.getElementById('noSearchResults');
    const searchResults = document.getElementById('searchResults');
    
    let visibleRows = 0;
    const totalRows = tableRows.length;
    
    tableRows.forEach(function(row) {
        const searchText = row.getAttribute('data-search-text') || '';
        
        if (searchTerm === '' || searchText.toLowerCase().includes(searchTerm)) {
            row.style.display = '';
            visibleRows++;
        } else {
            row.style.display = 'none';
        }
    });
    
    // Update search results text
    if (searchTerm === '') {
        searchResults.textContent = `ุนุฑุถ ุฌููุน ุงูุทูุงุจ (${totalRows})`;
        noResultsDiv.style.display = 'none';
    } else {
        searchResults.textContent = `ุนุฑุถ ${visibleRows} ูู ${totalRows} ุทุงูุจ`;
        noResultsDiv.style.display = visibleRows === 0 ? 'block' : 'none';
    }
    
    // Reset checkbox states after search
    const selectAllCheckbox = document.getElementById('selectAll');
    if (selectAllCheckbox) {
        selectAllCheckbox.checked = false;
        selectAllCheckbox.indeterminate = false;
    }
    updateSelectedCount();
}

function clearSearch() {
    const searchInput = document.getElementById('studentSearchInput');
    searchInput.value = '';
    searchStudents();
    searchInput.focus();
}

